export const Route = createRootRoute({
  head: () => ({
    meta: [
      { charSet: "utf-8" },
      { name: "viewport", content: "width=device-width, initial-scale=1" },
      { title: "Atlas Voyages — Curated Morocco Tours & Travel" },
      { name: "description", content: "Discover Morocco with Atlas Voyages. Private tours from Marrakech, Sahara, Atlas Mountains, Chefchaouen, Fes & Essaouira. Booked in MAD, in EN/FR/AR." },
    ],
    links: [{ rel: "stylesheet", href: appCss }],
  }),
  shellComponent: RootShell,
  component: RootComponent,
  notFoundComponent: NotFoundComponent,
});

function RootShell({ children }: { children: React.ReactNode }) {
  return (
    <html lang="en">
      <head><HeadContent /></head>
      <body>{children}<Scripts /></body>
    </html>
  );
}

function RootComponent() {
  return (
    <I18nProvider>
      <Header />
      <main className="min-h-screen pt-20">
        <Outlet />
      </main>
      <Footer />
    </I18nProvider>
  );
}
